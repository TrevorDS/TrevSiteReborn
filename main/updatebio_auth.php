////////////////////////////////////////////////////////////////////////////////
// CONSTANTS
////////////////////////////////////////////////////////////////////////////////
global $DB_SERVER_NAME;
global $DB_USERNAME;
global $DB_PASSWORD;
global $DB_NAME;

$TABLE = "user_data";
$UserID = $_POST["UserID"];
$CALLBACK = $PAGES . "profile?user=$UserID";

if (!isset($UserID) || $UserID === null) {
    $CALLBACK = $DEFAULT_CALLBACK_REDIRECT;
}

////////////////////////////////////////////////////////////////////////////////
// Variables
////////////////////////////////////////////////////////////////////////////////
$NEW_BIO = FilterInput($_POST["updateBio"]);

////////////////////////////////////////////////////////////////////////////////
// Functions
////////////////////////////////////////////////////////////////////////////////

function ValidateInput($bio) {

    global $CALLBACK;

    if ($bio === "" || strlen($bio) === 0) {
        header("Location: $CALLBACK");
        exit;
    }
}

////////////////////////////////////////////////////////////////////////////////
// INIT
////////////////////////////////////////////////////////////////////////////////
// POST VERIFICATION
if (!$_POST) {
    header("Location: $CALLBACK");
    exit;
}

ValidateInput($NEW_BIO);

////////////////////////////////////////////////////////////////////////////////
// RUN-TIME
////////////////////////////////////////////////////////////////////////////////
// Create connection
$conn = new mysqli($DB_SERVER_NAME, $DB_USERNAME, $DB_PASSWORD, $DB_NAME);

// Check connection
if ($conn->connect_error) {
    header("Location: $CALLBACK");
    exit;
}

$sql = "UPDATE $TABLE SET bio='" . $NEW_BIO . "' WHERE user_id='$UserID'";

// Run the SQL Command
$conn->query($sql);

$conn->close();


header("Location: $CALLBACK");
exit;
